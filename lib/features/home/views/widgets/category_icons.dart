void selectCategoryIcon(
    BuildContext context, void Function(IconData? selectedIcon) selectedIcon) {
  showDialog(
      context: context,
      builder: (context) {
        return Dialog(
          alignment: Alignment.center,
          child: SizedBox(
            height: 224,
            child: GridView(
              gridDelegate: const SliverGridDelegateWithFixedCrossAxisCount(
                  crossAxisCount: 4, mainAxisSpacing: 16, crossAxisSpacing: 16),
              children: List.generate(iconList.length, (index) {
                return InkWell(
                    onTap: () {
                      selectedIcon(iconList[index]);
                      Navigator.pop(context);
                    },
                    child: Icon(iconList[index]));
              }),
            ),
          ),
        );
      });
}
